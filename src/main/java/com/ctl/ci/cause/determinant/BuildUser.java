package com.ctl.ci.cause.determinant;

/**
 * @author AB40286
 *
 */
public class BuildUser {

	private String buildUserFullName;
	private String buildUserFirstName;
	private String buildUserLastName;
	private String buildUserEmail;
	private String buildUserId;

	public String getBuildUserFullName() {
		return buildUserFullName;
	}

	public void setBuildUserFullName(String buildUserFullName) {
		this.buildUserFullName = buildUserFullName;
	}

	public String getBuildUserFirstName() {
		return buildUserFirstName;
	}

	public void setBuildUserFirstName(String buildUserFirstName) {
		this.buildUserFirstName = buildUserFirstName;
	}

	public String getBuildUserLastName() {
		return buildUserLastName;
	}

	public void setBuildUserLastName(String buildUserLastName) {
		this.buildUserLastName = buildUserLastName;
	}

	public String getBuildUserEmail() {
		return buildUserEmail;
	}

	public void setBuildUserEmail(String buildUserEmail) {
		this.buildUserEmail = buildUserEmail;
	}

	public String getBuildUserId() {
		return buildUserId;
	}

	public void setBuildUserId(String buildUserId) {
		this.buildUserId = buildUserId;
	}

}
